        <div class="guide-tip">
            <strong>Un silo SEO</strong> organise vos contenus par thématique avec :
            1 article PILIER + 5-10 articles SATELLITES + Maillage interne entre eux.
        </div>

        <div class="guide-formula" style="white-space: pre-wrap;">SILO : Vendre son Bien
│
├── ARTICLE PILIER : Guide Complet Vendre sa Maison [VILLE]
│
├── Article satellite 1 : Estimer le prix de sa maison
├── Article satellite 2 : Préparer sa maison pour la vente
├── Article satellite 3 : Home staging à petit budget
├── Article satellite 4 : Photos immobilières qui vendent
├── Article satellite 5 : Négociation et compromis de vente
└── Article satellite 6 : Diagnostics immobiliers obligatoires</div>

        <div class="guide-tip">
            <strong>Règle d'or :</strong> Chaque article satellite doit OBLIGATOIREMENT faire un lien vers l'article pilier.
            L'article pilier fait des liens vers tous les satellites.
