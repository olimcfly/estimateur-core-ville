        <h2>PARTIE 3 : Structure Technique d'un Article</h2>

        <h3>1. SEO Title (Titre SEO) - 50-60 caractères</h3>
        <div class="guide-formula"><strong>Formule :</strong> [Mot-clé principal] : [Bénéfice] | [Ville]</div>
        <p>Exemples :</p>
        <ul>
            <li style="color: #0d7a3e;">Vendre Maison Sans Agent : Guide 2025 | Grenoble (55 car.)</li>
            <li style="color: #c0392b;">Comment bien vendre sa maison à Grenoble rapidement (77 car. - TROP LONG)</li>
        </ul>

        <h3>2. Meta Description - 150-160 caractères</h3>
        <div class="guide-formula"><strong>Formule :</strong> [Promesse] en [temps/méthode]. [Bénéfice secondaire]. [CTA]</div>

        <h3>3. URL (Slug) - 5-7 mots</h3>
        <ul>
            <li>Court et descriptif, contient le mot-clé principal</li>
            <li>Tirets entre les mots, pas d'accents, pas de mots vides</li>
            <li style="color: #0d7a3e;">votre-site.fr/vendre-maison-sans-agent-grenoble</li>
        </ul>

        <h3>4. Golden Ratio du Mot-Clé (1.618%)</h3>
        <div class="guide-tip">
            <strong>La densité idéale du mot-clé focus est de 1.618%</strong> (le Golden Ratio).<br>
            Plage optimale : 1.0% à 2.5%.<br><br>
            <table class="guide-table">
                <thead><tr><th>Longueur article</th><th>Occurrences idéales</th></tr></thead>
                <tbody>
                    <tr><td>800 mots</td><td>~13 occurrences</td></tr>
                    <tr><td>1500 mots</td><td>~24 occurrences</td></tr>
                    <tr><td>2000 mots</td><td>~32 occurrences</td></tr>
                    <tr><td>3000 mots</td><td>~49 occurrences</td></tr>
                </tbody>
            </table>
        </div>

        <h3>5. Structure des Titres</h3>
        <div class="guide-formula" style="white-space: pre-wrap;">H1 : Titre principal (1 seul)
  H2 : Grandes sections (4-6 par article)
    H3 : Sous-sections (2-3 par H2)
      H4 : Détails (si nécessaire)

Astuce : Intégrez des variantes du mot-clé dans les H2 et H3</div>

        <h3>6. Maillage Interne (CRUCIAL)</h3>
        <ul>
            <li>Minimum 3-5 liens internes par article</li>
            <li>Ancres descriptives (PAS "cliquez ici")</li>
            <li>Liens vers articles du même silo en priorité</li>
            <li>Placement : introduction, corps et conclusion</li>
        </ul>

        <h3>7. Images Optimisées</h3>
        <table class="guide-table">
            <thead><tr><th>Élément</th><th>Règle</th></tr></thead>
            <tbody>
                <tr><td>Nom de fichier</td><td>Descriptif, minuscules, tirets : vendre-maison-locale.jpg</td></tr>
                <tr><td>Balise ALT</td><td>10-15 mots descriptifs avec mot-clé naturel</td></tr>
                <tr><td>Format</td><td>WebP ou JPEG, max 100-200 Ko</td></tr>
                <tr><td>Dimensions</td><td>1200x800 px maximum</td></tr>
                <tr><td>Nombre</td><td>3-5 images par article, 1 image après chaque H2</td></tr>
            </tbody>
        </table>

        <h3>8. Section FAQ (5-10 questions)</h3>
        <div class="guide-tip">
            <strong>Pourquoi une FAQ :</strong>
            <ul style="margin: 0.5rem 0 0; padding-left: 1.5rem;">
                <li>Position 0 sur Google (featured snippet)</li>
                <li>Rich Snippets dans les résultats</li>
                <li>Capture les recherches en questions (longue traîne)</li>
            </ul>
            <br>
            <strong>Astuce :</strong> Utilisez "People Also Ask" de Google pour trouver les vraies questions.
        </div>
    </section>

