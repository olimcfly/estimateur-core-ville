        <h2>PARTIE 1 : Stratégie de Mots-Clés Locaux</h2>

        <h3>Pourquoi Commencer par des Mots-Clés à Faible Concurrence</h3>
        <p>Quand vous débutez en SEO, attaquer directement des mots-clés comme "agent immobilier Paris" est suicidaire. La stratégie gagnante : <strong>Ciblez des mots-clés de longue traîne hyper-localisés et spécialisés.</strong></p>

        <table class="guide-table">
            <thead>
                <tr><th>Type</th><th>Exemple</th><th>Concurrence</th></tr>
            </thead>
            <tbody>
                <tr><td>Trop générique</td><td>agent immobilier Lyon</td><td style="color: #c0392b;">TRÈS ÉLEVÉE</td></tr>
                <tr><td>Mieux mais insuffisant</td><td>vendre appartement Lyon 3</td><td style="color: #D4AF37;">ÉLEVÉE</td></tr>
                <tr><td style="color: #0d7a3e; font-weight: 600;">PARFAIT (Longue traîne)</td><td>vendre appartement sans travaux Lyon 3 Montchat</td><td style="color: #0d7a3e;">FAIBLE</td></tr>
            </tbody>
        </table>

        <div class="guide-formula">
            <strong>Formule :</strong> [Action] + [Type de bien] + [Spécificité] + [Ville] + [Quartier]
        </div>

        <h3>Exemples de mots-clés gagnants</h3>
        <ul>
            <li>vendre maison rapidement sans agent Grenoble Bastille</li>
            <li>estimer appartement 3 pièces Lyon 6 Foch</li>
            <li>investissement locatif rentable étudiant Toulouse Capitole</li>
            <li>primo accédant aide PTZ Nantes Erdre</li>
            <li>vendre appartement senior résidence services centre-ville</li>
        </ul>
